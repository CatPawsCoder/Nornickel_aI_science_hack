# -*- coding: utf-8 -*-
"""Прогон 10 вопросов от экспертов кейса (выданы организаторами) на живом демо."""
import json
import sys
import time
import urllib.request

sys.stdout.reconfigure(encoding="utf-8")

BASE = "https://catpawws-ai-science-hack.hf.space"

QUESTIONS = [
    ("1 ТЭО обессоливания", "Технико-экономическое сравнение вариантов подготовки воды (обессоливания) для обогатительной фабрики. Требования к воде: содержание сульфатов, хлоридов, кальция, магния, натрия — 200-300 мг/л каждого; сухой остаток — 1000 мг/дм3. Исходная вода из скважины содержит на порядок больше солей."),
    ("2 очистка шахтных вод", "Выполнить литературный обзор методов очистки шахтных вод горно-рудных предприятий цветной металлургии. Отечественная и мировая практика."),
    ("3 циркуляция католита", "Литературный обзор существующих в мировой практике технических решений для организации циркуляции католита при производстве никелевых катодов методом электроэкстракции и её оптимальной скорости."),
    ("4 электролиз: ванны", "Обзор технических решений в области электролитического производства никеля, меди, кобальта по организации: подачи электролита в ванны, движения потока раствора в ванне, циркуляции электролита, вывода электролита из ванны, конструкции диафрагменных ячеек (анодные и катодные диафрагмы)."),
    ("5 техногенный гипс", "Провести литературный обзор источников техногенного гипса и способов его переработки. Отечественная и зарубежная практика."),
    ("6 закачка шахтных вод", "Анализ технологий и примеров закачки шахтных вод горно-рудных предприятий России и мира в глубокие горизонты."),
    ("7 уголь для закладки", "Обзор практик использования угля и отходов угольной промышленности для закладки выработанного пространства."),
    ("8 удаление SO2", "Обзор способов удаления SO2 из отходящих газов металлургических предприятий мира."),
    ("9 Au/Ag/МПГ штейн-шлак", "Распределение Au, Ag и МПГ между медным, никелевым штейном и шлаком по зарубежным источникам последних лет."),
    ("10 свинцово-цинковое", "Обзор современных способов переработки свинцово-цинкового сырья. Мировая практика."),
]

for i, (tag, q) in enumerate(QUESTIONS, 1):
    t0 = time.time()
    try:
        req = urllib.request.Request(BASE + "/api/query",
            data=json.dumps({"text": q}).encode(),
            headers={"Content-Type": "application/json"})
        r = json.loads(urllib.request.urlopen(req, timeout=150).read())
        a = r["answer"]
        md = a["markdown"]
        direct = md.split("##")[1].replace("✅ Прямой ответ", "").strip() if "Прямой ответ" in md else "(нет прямого ответа)"
        direct_one = " ".join(direct.split())[:170]
        empty = "нет информации" in direct.lower() or "не упоминаются" in direct.lower() or "отсутству" in direct.lower()[:120]
        mark = "⚠️ " if empty else "✅"
        print(f"{mark} [{i:2d}] {tag:24s} {time.time()-t0:4.1f}s pubs={a['n_publications']:2d} conds={a['n_conditions']:3d} claims={a['n_claims']:2d} эксп={'да' if 'Эксперты' in md else '—'}")
        print(f"        → {direct_one}")
    except Exception as e:
        print(f"❌ [{i:2d}] {tag}: {repr(e)[:90]}")
