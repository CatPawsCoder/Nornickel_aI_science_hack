# -*- coding: utf-8 -*-
"""Прогон 20 тестовых вопросов по типам из ТЗ на живом демо.

Для части вопросов заданы GOLDEN-проверки: ожидаемые числа/подстроки,
которые обязаны присутствовать в ответе (не только «что-то нашлось»).
"""
import json
import sys
import time
import urllib.request

sys.stdout.reconfigure(encoding="utf-8")

BASE = "https://catpawws-ai-science-hack.hf.space"

# tag -> список подстрок, обязанных быть в markdown-ответе
GOLDEN = {
    "ТЗ-1 обессоливание": ["HiPRO"],
    "число-диапазон": ["60"],            # 60-65°C
    "число-оптимум": ["А/м2"],           # плотность тока с единицей
    "термин HiPRO": ["98"],              # извлечение воды 98%
    "термин SULFATEQ": ["300"],          # <300 мг/л
    "факт-дендриты": ["240"],            # выше 240 А/м2
    "аббревиатура": ["взвешенной плавки"],
}

QUESTIONS = [
    # --- 4 эталонных из ТЗ ---
    ("ТЗ-1 обессоливание", "Какие методы обессоливания воды подходят, если вода содержит сульфаты и хлориды 200-300 мг/л, а требуемый сухой остаток не более 1000 мг/дм3?"),
    ("ТЗ-2 католит", "Какие технические решения организации циркуляции католита при электроэкстракции никеля описаны в мировой практике, и какая скорость потока считается оптимальной?"),
    ("ТЗ-3 МПГ штейн/шлак", "Покажите эксперименты и публикации по распределению Au, Ag и МПГ между медным никелевым штейном и шлаком за последние 10 лет"),
    ("ТЗ-4 закачка вод", "Какие способы закачки шахтных вод в глубокие горизонты применялись в России и за рубежом?"),
    # --- числовые ограничения ---
    ("число <200", "Какие решения существуют для очистки воды при концентрации сульфатов менее 200 мг/л?"),
    ("число-диапазон", "Какая температура электролита поддерживается при электроэкстракции никеля?"),
    ("число-оптимум", "Какая плотность тока оптимальна при электроэкстракции никеля?"),
    # --- география ---
    ("гео-сравнение", "Сравните отечественную и зарубежную практику флотации шлаков медеплавильного производства"),
    ("гео-зарубеж", "Какая зарубежная практика обеднения никелевых шлаков существует?"),
    # --- временной диапазон ---
    ("годы", "Какие публикации по переработке медных шлаков вышли за последние 5 лет?"),
    # --- RU/EN и аббревиатуры ---
    ("EN-термин", "Что известно про electrowinning никеля?"),
    ("аббревиатура", "Что такое ПВП и где применяется печь взвешенной плавки?"),
    # --- конкретные факты/термины вне словаря ---
    ("термин HiPRO", "Что такое трехстадийный процесс HiPRO?"),
    ("термин SULFATEQ", "Что за биологический процесс SULFATEQ и какие концентрации сульфатов он обеспечивает?"),
    ("факт-дендриты", "При какой плотности тока образуются дендритные или порошкообразные осадки?"),
    # --- оборудование и процессы ---
    ("оборудование", "Какие конструкции диафрагменных ячеек используются при электролизе никеля?"),
    ("процесс SO2", "Какие способы удаления SO2 из отходящих газов существуют и какова степень очистки?"),
    ("шламы", "Как извлекают благородные металлы из шламов металлургического производства?"),
    # --- пробелы и противоречия ---
    ("пробел", "Что известно про кучное выщелачивание никелевой руды в холодном климате?"),
    ("экономика", "Какие технико-экономические показатели очистки шахтных вод известны?"),
]

results = []
for i, (tag, q) in enumerate(QUESTIONS, 1):
    t0 = time.time()
    try:
        req = urllib.request.Request(BASE + "/api/query",
            data=json.dumps({"text": q}).encode(),
            headers={"Content-Type": "application/json"})
        r = json.loads(urllib.request.urlopen(req, timeout=120).read())
        a = r["answer"]
        md = a["markdown"]
        dt = time.time() - t0
        has_direct = "Прямой ответ" in md
        direct = ""
        if has_direct:
            direct = md.split("##")[1].replace("✅ Прямой ответ", "").strip().split("\n")[0][:110]
        ok = has_direct and (a["n_claims"] > 0 or a["n_conditions"] > 0 or a["n_publications"] > 0)
        # golden-проверка: ожидаемые числа/подстроки обязаны быть в ответе
        golden_ok = True
        missing = []
        for needle in GOLDEN.get(tag, []):
            if needle not in md:
                golden_ok = False
                missing.append(needle)
        ok = ok and golden_ok
        results.append((tag, dt, a["n_publications"], a["n_conditions"], a["n_claims"], has_direct, ok))
        mark = "✅" if ok else "⚠️"
        gold = " GOLDEN-FAIL:" + ",".join(missing) if missing else (" [golden ok]" if tag in GOLDEN else "")
        print(f"{mark} [{i:2d}] {tag:20s} {dt:4.1f}s pubs={a['n_publications']:2d} conds={a['n_conditions']:3d} claims={a['n_claims']:2d}{gold}")
        print(f"       → {direct}")
    except Exception as e:
        results.append((tag, 0, 0, 0, 0, False, False))
        print(f"❌ [{i:2d}] {tag:20s} ERROR: {repr(e)[:80]}")

n_ok = sum(1 for r in results if r[6])
avg = sum(r[1] for r in results if r[1]) / max(1, len([r for r in results if r[1]]))
print(f"\n===== ИТОГ: {n_ok}/20 ok, среднее время {avg:.1f}s =====")
